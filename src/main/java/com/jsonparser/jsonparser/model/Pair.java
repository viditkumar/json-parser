package com.jsonparser.jsonparser.model;

public class Pair {
	String x;
	String y;
	
	public Pair(String x, String y){
		this.x = x;
		this.y = y;
	}

	public String getX() {
		return x;
	}

	public void setX(String x) {
		this.x = x;
	}

	public String getY() {
		return y;
	}

	public void setY(String y) {
		this.y = y;
	}
	
	
}
